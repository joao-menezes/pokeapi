import turtle
from turtle import *

turtle.screensize(canvwidth=1200, canvheight=1200, bg="blue") 
tart = turtle.Turtle()
turtle.speed(10)
showturtle()

tart.shape("turtle")
tart.sety(100)
tart.setpos(100,200)
tart.setpos(100,0)
tart.setpos(100,200)
tart.setpos(200,200)
tart.setpos(200,0)
tart.setpos(200,400)
tart.setpos(300,400)
tart.setpos(300,0)
tart.setpos(300,200)
tart.setpos(400,200)
tart.setpos(400,0)
tart.setpos(400,200)
tart.setpos(500,200)
